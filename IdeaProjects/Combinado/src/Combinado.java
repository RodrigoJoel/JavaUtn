public class Combinado {
    public static void main(String[] args) {
        double a = 6;
        double b = 5;
        double c = 2;
        double d = 3;
        double e = 7;
        double h = 30;
        double i = 1;

        double multiplicacion = c/d * e/c;
        double resta = a/b - multiplicacion;
        double suma = resta + c/h;
        double division = i/d / b;
        double resultado = suma / division;

        System.out.println(multiplicacion);
        System.out.println(resta);
        System.out.println(suma);
        System.out.println(division);
        System.out.println("El resultado final de la operacion es "+resultado);
    }
}
